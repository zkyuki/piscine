#include <unistd.h>

int	main(int argc, char **argv)
{
	int	k;

	k = 0;
	argc++;
	while (argv[0][k] != '\0')
	{
		write(1, &argv[0][k], 1);
		k++;
	}
	write(1, "\n", 1);
	return (0);
}
