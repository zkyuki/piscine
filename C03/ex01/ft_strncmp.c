int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	k;

	if (n == 0)
		return (0);
	k = 0;
	while (s1[k] != '\0' && k < n - 1)
	{
		if (s1[k] != s2[k])
			return (s1[k] - s2[k]);
		k++;
	}
	return (s1[k] - s2[k]);
}
