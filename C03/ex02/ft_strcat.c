char	*ft_strcat(char *dest, char *src)
{
	int	k;
	int	z;

	k = 0;
	z = 0;
	while (dest[k] != '\0')
		k++;
	while (src[z] != '\0')
	{
		dest[k] = src[z];
		k++;
		z++;
	}
	dest[k] = '\0';
	return (dest);
}
