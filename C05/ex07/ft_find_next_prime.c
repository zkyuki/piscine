int	ft_find_next_prime(int nb)
{
	int	k;

	k = 2;
	if (nb <= 1)
		return (2);
	while (k <= nb / k)
	{
		if (nb % k == 0)
		{
			nb++;
			k = 1;
		}
		k++;
	}
	return (nb);
}
